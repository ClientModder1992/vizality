const { React, getModule, getModuleByDisplayName } = require('powercord/webpack');
const AsyncComponent = require('../AsyncComponent');
const FormItem = require('./FormItem.jsx');

const Radio = AsyncComponent.from(getModuleByDisplayName('RadioGroup'));

let margin = '';
module.exports = class RadioGroup extends React.Component {
  constructor () {
    super();
    this.state = { margin };
  }

  async componentDidMount () {
    if (margin !== '') {
      return;
    }

    margin = (await getModule([ 'marginTop20' ])).marginTop20;
    this.setState({ margin });
  }

  render () {
    const { children: title, note, required } = this.props;
    return (
      <FormItem title={title} note={note} required={required}>
        <Radio {...this.props}/>
      </FormItem>
    );
  }
};

