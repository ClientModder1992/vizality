const { getModuleByDisplayName, React } = require('powercord/webpack');
const AsyncComponent = require('../AsyncComponent');
const FormItem = require('./FormItem.jsx');

const Search = AsyncComponent.from(getModuleByDisplayName('SearchBar'));

module.exports = class SearchBar extends React.Component {
  render () {
    const { children: title, note, required } = this.props;
    delete this.props.children;

    return (
      <FormItem title={title} note={note} required={required}>
        <Search {...this.props}/>
      </FormItem>
    );
  }
};

